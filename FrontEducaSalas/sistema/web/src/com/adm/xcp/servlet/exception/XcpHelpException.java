package com.adm.xcp.servlet.exception;

public class XcpHelpException extends Exception {

	public XcpHelpException() {
		super();
	}

	public XcpHelpException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public XcpHelpException(String message, Throwable cause) {
		super(message, cause);
	}

	public XcpHelpException(String message) {
		super(message);
	}

	public XcpHelpException(Throwable cause) {
		super(cause);
	}

}
